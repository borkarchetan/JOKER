// date object
let date = new Date();

document.write(date);


document.write("current miliseconds: ", date.getTime()); // it shows miliseconds
document.write("<br>");


document.write("current year: ", date.getFullYear()); // it shows full year
document.write("<br>");


document.write("current month: ", date.getMonth()); // it shows month
document.write("<br>");


document.write("current date: ", date.getDate()); // it shows date
document.write("<br>");


document.write("current hour: ", date.getHours()); // it shows hours
document.write("<br>");


document.write("current minute: ", date.getMinutes()); // it shows minutes
document.write("<br>");


document.write("current second: ", date.getSeconds()); // it shows seconds
document.write("<br>");


document.write("current day: ", date.getDay()); // it shows day 1 to 7
document.write("<br>");


